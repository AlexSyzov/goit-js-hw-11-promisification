const refs = {
  btn: document.querySelector(".js-button"),
  input: document.querySelector(".js-input"),
  container: document.querySelector(".flex-js"),
};

refs.btn.addEventListener("click", onButtonClick);
window.addEventListener("keydown", onEnterClick);

function onButtonClick() {
  refs.container.innerHTML = "";
  const name = refs.input.value;

  for (let i = 0; i < name.length; i += 1) {
    const letter = name.slice(i, i + 1);
    const index = Math.floor(Math.random() * 30);

    const secondPart = letters[letter.toUpperCase()][index].slice(1);

    const newLeftDiv = document.createElement("div");
    newLeftDiv.classList.add("flex-left");
    newLeftDiv.textContent = letter.toUpperCase();
    refs.container.appendChild(newLeftDiv);

    const newRightDiv = document.createElement("div");
    newRightDiv.classList.add("flex-right");
    newRightDiv.textContent = secondPart;
    refs.container.appendChild(newRightDiv);
  }
}

function onEnterClick(event) {
  if (event.keyCode === 13) {
    onButtonClick();
  }
}
